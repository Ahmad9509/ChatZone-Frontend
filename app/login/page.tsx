// Login page with OAuth and Email/Password authentication
'use client';

import { useEffect, useState } from 'react';
import { useRouter } from 'next/navigation';
import axios from 'axios';

const API_URL = process.env.NEXT_PUBLIC_API_URL || 'https://chatzone-api-b8h3g0c4hydccrcy.eastus-01.azurewebsites.net';

export default function LoginPage() {
  const router = useRouter();
  const [isSignUp, setIsSignUp] = useState(false);
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [name, setName] = useState('');
  const [error, setError] = useState('');
  const [loading, setLoading] = useState(false);

  useEffect(() => {
    // Check if already logged in (only on client side)
    if (typeof window !== 'undefined') {
      const token = localStorage.getItem('token');
      if (token) {
        router.push('/chat');
      }
    }
  }, [router]);

  const handleGoogleLogin = () => {
    window.location.href = `${API_URL}/api/auth/google`;
  };

  const handleTwitterLogin = () => {
    window.location.href = `${API_URL}/api/auth/twitter`;
  };

  const handleEmailAuth = async (e: React.FormEvent) => {
    e.preventDefault();
    setError('');
    setLoading(true);

    try {
      const endpoint = isSignUp ? '/api/auth/register' : '/api/auth/login';
      const payload = isSignUp ? { email, password, name } : { email, password };

      const response = await axios.post(`${API_URL}${endpoint}`, payload);

      if (response.data.success && response.data.token) {
        localStorage.setItem('token', response.data.token);
        router.push('/chat');
      } else {
        setError(response.data.error || 'Authentication failed');
      }
    } catch (err: any) {
      setError(err.response?.data?.error || 'Something went wrong');
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="flex min-h-screen items-center justify-center bg-background px-4 text-text-primary">
      <div className="w-full max-w-md rounded-2xl border-0 bg-surface p-8 soft-elevated">
        <div className="mb-8 text-center">
          <h1 className="text-2xl font-semibold">
            {isSignUp ? 'Create your account' : 'Welcome back'}
          </h1>
          <p className="mt-2 text-sm text-text-secondary">
            {isSignUp ? 'Sign up to start chatting with AI.' : 'Sign in to continue to ChatZone.'}
          </p>
        </div>

        <form onSubmit={handleEmailAuth} className="space-y-4">
          {isSignUp && (
            <div>
              <label className="block text-xs font-semibold uppercase text-text-secondary">Name</label>
              <input
                type="text"
                value={name}
                onChange={(e) => setName(e.target.value)}
                className="mt-1 w-full rounded-xl border-0 bg-surface-subtle px-4 py-3 text-sm soft-pressed focus:outline-none focus:ring-2 focus:ring-accent focus:ring-offset-2 neumorphic-transition"
                placeholder="Your name"
                required
              />
            </div>
          )}

          <div>
            <label className="block text-xs font-semibold uppercase text-text-secondary">Email</label>
            <input
              type="email"
              value={email}
              onChange={(e) => setEmail(e.target.value)}
              className="mt-1 w-full rounded-xl border-0 bg-surface-subtle px-4 py-3 text-sm soft-pressed focus:outline-none focus:ring-2 focus:ring-accent focus:ring-offset-2 neumorphic-transition"
              placeholder="name@example.com"
              required
            />
          </div>

          <div>
            <label className="block text-xs font-semibold uppercase text-text-secondary">Password</label>
            <input
              type="password"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              className="mt-1 w-full rounded-xl border-0 bg-surface-subtle px-4 py-3 text-sm soft-pressed focus:outline-none focus:ring-2 focus:ring-accent focus:ring-offset-2 neumorphic-transition"
              placeholder="••••••••"
              minLength={8}
              required
            />
            {isSignUp && (
              <p className="mt-1 text-xs text-text-secondary">At least 8 characters</p>
            )}
          </div>

          {error && (
            <div className="rounded-xl border-0 bg-red-500/10 px-4 py-3 text-sm text-red-500 soft-elevated">
              {error}
            </div>
          )}

          <button
            type="submit"
            disabled={loading}
            className="w-full rounded-xl border-0 bg-accent px-4 py-3 text-sm font-semibold text-white soft-elevated neumorphic-transition hover:soft-hover active:soft-pressed disabled:cursor-not-allowed disabled:opacity-60"
          >
            {loading ? 'Please wait…' : isSignUp ? 'Create account' : 'Sign in'}
          </button>
        </form>

        <div className="mt-6 text-center">
          <button
            onClick={() => {
              setIsSignUp(!isSignUp);
              setError('');
            }}
            className="text-sm font-semibold text-accent hover:underline neumorphic-transition"
          >
            {isSignUp ? 'Already have an account? Sign in' : "Need an account? Sign up"}
          </button>
        </div>

        <div className="my-6 flex items-center gap-3">
          <div className="h-px flex-1 bg-border" />
          <span className="text-xs uppercase tracking-wide text-text-secondary">or continue with</span>
          <div className="h-px flex-1 bg-border" />
        </div>

        <div className="flex items-center justify-center gap-4">
          <button
            onClick={handleGoogleLogin}
            className="flex h-12 w-12 items-center justify-center rounded-full border-0 bg-surface soft-elevated neumorphic-transition hover:soft-hover active:soft-pressed"
            aria-label="Sign in with Google"
          >
            <svg className="h-6 w-6" viewBox="0 0 24 24">
              <path
                fill="#4285F4"
                d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
              />
              <path
                fill="#34A853"
                d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
              />
              <path
                fill="#FBBC05"
                d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
              />
              <path
                fill="#EA4335"
                d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
              />
            </svg>
          </button>

          <button
            onClick={handleTwitterLogin}
            className="flex h-12 w-12 items-center justify-center rounded-full border-0 bg-surface soft-elevated neumorphic-transition hover:soft-hover active:soft-pressed"
            aria-label="Sign in with Twitter"
          >
            <svg className="h-6 w-6" fill="currentColor" viewBox="0 0 24 24">
              <path d="M18.244 2.25h3.308l-7.227 8.26 8.502 11.24H16.17l-5.214-6.817L4.99 21.75H1.68l7.73-8.835L1.254 2.25H8.08l4.713 6.231zm-1.161 17.52h1.833L7.084 4.126H5.117z" />
            </svg>
          </button>
        </div>

        <p className="mt-8 text-center text-xs text-text-secondary">
          By signing in, you agree to our Terms of Service and Privacy Policy.
        </p>
      </div>
    </div>
  );
}

